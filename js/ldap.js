$(document).ready(function (){

        $("a").mouseover(function(i){
            
            //alert($(this).attr("name"));
            
            if($(this).attr("href")=="#") ;
            else $(this).css({ color: "red"});
            
            
        });
        
        $("a").mouseleave()()(function(i){
            
            //alert($(this).attr("name"));
            
            if($(this).attr("href")=="#") ;
            else $(this).css({ color: "blue"});
            
            
        });




});

